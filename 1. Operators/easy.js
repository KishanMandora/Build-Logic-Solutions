// program to  add 5 numbers

const addFiveNumbers = (n1, n2, n3, n4, n5) => n1 + n2 + n3 + n4 + n5;

// program to find if number is odd or even

const oddOrEven = (num) => (num % 2 === 0 ? `${num} is even` : `${num} is odd`);

